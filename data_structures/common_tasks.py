# Приложение работает на основании данных, которые ввёл пользователь,
# и словаря. Ключ словаря — название продукта в нижнем регистре,
# а значение — количество калорий на 100 грамм этого продукта.
# Для выполнения задачи напишите функцию jrun(), которая принимает:
# food_name — строку, обозначающую название продукта. Может
# быть введено в любом регистре, в том числе и смешанном.
# food_weight — количество продукта в граммах.
# calories_dict — словарь — <название продукта>: <количество
# калорий на 100 грамм>.
# Когда пользователь ввёл название продукта, программа проверяет,
# есть ли такой продукт в словаре. Если есть — считает количество
# калорий с учётом его веса, и возвращает строку в формате:
# Количество калорий на <food_weight> грамм в продукте “<food_name>”
# равно <результат вычислений>.
# Если продукта в словаре нет, то:
# Добавьте в словарь новый ключ food_name.
# Заполните значение случайным целым числом в промежутке от 100 до 600.
# Используйте новое значение для расчёта результата, а
# результат верните в том же формате.


def jrun(food_name: str, food_weight, calories_dict: dict):
    low_food = food_name.lower()
    if low_food in calories_dict:
        cal_in_food = food_weight/100 * calories_dict[low_food]
        return (f'Количество калорий на {food_weight} грамм в '
            f'продукте “{food_name}” равно {cal_in_food}.'
        )
    else:
        calories_dict[low_food] = randint(100, 600)
        cal_in_food = food_weight/100 * calories_dict[low_food]
        return (f'Количество калорий на {food_weight} '
                f'грамм в продукте “{food_name}” равно {cal_in_food}.'
                )


