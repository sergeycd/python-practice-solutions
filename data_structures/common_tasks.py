# Вручную сравнивать скучно и долго, нужна программа.
# Напишите эту программу: она должна сравнивать два списка и возвращать
# набор элементов, которые есть в первом списке, но которых нет во втором;
# набор элементов, которые есть во втором списке, но которых нет в первом;
# набор элементов, которые присутствуют в обоих списках.
# Например, если в программу переданы списки


collection_1 = ['камень', 'ножницы', 'бумага', 'карандаш']
collection_2 = ['карандаш', 'огонь', 'вода', 'огонь']
# …то программа должна вернуть три коллекции, в которых будут элементы

# # Первая коллекция: элементы, которые есть
# # только в списке collection_1
# 'бумага', 'камень', 'ножницы'

# # Вторая коллекция:  элементы, которые есть
# # только в списке collection_2
# 'вода', 'огонь'

# # Третья коллекция: элементы, которые есть
# # одновременно и в списке collection_1, и в списке collection_2
# 'карандаш'
# Напишите тело функции get_stickers_comparison(), которая принимает два
# параметра: collection_1 и collection_2 — два списка,
# которые требуется сравнить.
# Функция должна вернуть кортеж из трёх коллекций:
# перечень стикеров, которые есть только в списке collection_1;
# перечень стикеров, которые есть только в списке collection_2;
# перечень стикеров, которые одновременно есть и в списке
# collection_1, и в списке collection_2.
# В пределах каждой из возвращаемых коллекций не должно
# быть дублирующихся элементов.
# Возвращаемые коллекции должны быть отсортированы по алфавиту
# (по возрастанию, от А к Я).


def get_stickers_comparison(collection_1, collection_2):
    set_1 = set(collection_1)
    set_2 = set(collection_2)
    unic_stickers_in_1 = set_1 - set_2
    unic_stickers_in_2 = set_2 - set_1
    common_collection = set_1 & set_2
    return (sorted(unic_stickers_in_1), sorted(unic_stickers_in_2),
            sorted(common_collection))


# Палиндром — это слово, фраза, число или другая
# последовательность символов, которая читается одинаково
# как в прямом, так и в обратном направлении. При чтении палиндрома
# игнорируются пробелы, знаки препинания и регистр символов.
# Примеры палиндромов:
# Ужа сопливого в ил посажу
# 111 11 111 1
# Ушами машу
# Тонет енот 
# Напишите функцию, которая принимает строку и проверяет,
# является ли переданная строка палиндромом. Если полученная строка — палиндром,
# функция должна вернуть True, в ином случае — False.
# На вход функции будут подаваться строки, которые содержат только цифры, буквы и пробелы.
# Требования:
# В решении должны использоваться срезы строк.
# При проверке строки программа должна игнорировать регистр символов и пробелы.

def pal_list(palindrom):
    pal_list = [x for x in list(palindrom) if x != ' ']
    copy_pal_list = pal_list.copy()
    copy_pal_list.reverse()
    return ''.join(copy_pal_list).lower() == ''.join(pal_list).lower()


print(pal_list('Тонет енот'))
